largura = float(input("Por favor, insira a largura da piscina em metros: "))
comprimento = float(input("Por favor, insira o comprimento da piscina em metros: "))
profundidade = float(input("Por favor, insira a profundidade da piscina em metros: "))
volume = largura * comprimento * profundidade
print("O volume da piscina é:", volume, "metros cúbicos (m³)")